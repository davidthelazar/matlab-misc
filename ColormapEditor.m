classdef ColormapEditor < handle
    properties (Constant = true, GetAccess = private)
        colorMat = getColorMat;
    end
    properties
        cmap
        prevMap
        showMeFlag
    end
    properties(GetAccess = private)
        figHandle = [];
    end
    methods
        function obj = ColormapEditor
            obj.showMeFlag = false;
            obj.reset;
        end
        function reset(obj)
            obj.cmap = ColormapEditor.allColors;
        end
        function undo(obj)
            obj.cmap = obj.prevMap{1};
            obj.prevMap(1:2) = [];% = obj.prevMap(2:end);
        end
        function setHistory(obj)
            obj.prevMap = [{obj.cmap},obj.prevMap];
            if numel(obj.prevMap)>10
                obj.prevMap(10:end) = [];
            end
        end
    
        function sort(obj,orderStr,sortDirs)
            orderStr = lower(orderStr);
            if nargin<3
                sortDirs = ones(size(orderStr));
            end
            scaleV = zeros(1,6);
            scalars = (10^3).^(2:-1:-3);
            for idx = 1:numel(orderStr)
                scaleV(strfind('rgbhsv',orderStr(idx))) = scalars(idx).*sortDirs(idx);
            end
            cmapScaled = [obj.cmap,rgb2hsv(obj.cmap)].*repmat(scaleV,size(obj.cmap,1),1);
            [garbage,ind] = sort(sum(cmapScaled,2));
            obj.cmap = obj.cmap(ind,:);
        end
        function set.cmap(obj,input)
            obj.setHistory;
            obj.cmap = input;
            if obj.showMeFlag
                obj.showMe;
            end
        end
        function sortSum(obj)
            [garbage,ind] = sort(sum(obj.cmap,2));
            obj.cmap = obj.cmap(ind,:);
        end
        function removeGrays(obj,tol)
            if nargin<2
                tol = 0.1;
            end
            
            ind = findGrays(obj.cmap,tol);
            obj.cmap = obj.cmap(~ind,:);
        end
        function onlyGrays(obj,tol)
            if nargin<2
                tol = 0.1;
            end
            ind = findGrays(obj.cmap,tol);
            obj.cmap = obj.cmap(ind,:);
        end
        function removeYellows(obj,hueScale,brightTol)
            if nargin<2
                hueScale = 1;
            end
            if nargin < 3
                brightTol = 0.1;
            end
            
            ind = findYellows(obj.cmap,hueScale,brightTol);            
            obj.cmap = obj.cmap(~ind,:);
        end
        function onlyYellows(obj,hueScale,brightTol)
            if nargin<2
                hueScale = 1;
            end
            if nargin < 3
                brightTol = 0.1;
            end
            
            ind = findYellows(obj.cmap,hueScale,brightTol);
            obj.cmap = obj.cmap(ind,:);
        end
        function filterBy(obj,filterStr,range)
            mapTemp = [obj.cmap,rgb2hsv(obj.cmap)];
            mapTemp = mapTemp(:,strfind('rgbhsv',filterStr));
            ind = (range(1)<=mapTemp)&(mapTemp<=range(2));
            obj.cmap = obj.cmap(ind,:);
        end
        function showMe(obj,flagInput)
            if nargin>1
               if strcmpi(flagInput,'on')
                   obj.showMeFlag = true;
               else
                   obj.showMeFlag = false;
               end
            
            else
                if isempty(obj.figHandle)
                    obj.figHandle = figure;
                end
                figure(obj.figHandle);
                colormap(obj.cmap);
                imagesc(1:length(obj.cmap),ones(1,length(obj.cmap)),1:length(obj.cmap));
                commandwindow;
            end
        end
        
        function thinOut(obj,num)
            ind = round(linspace(1,length(obj.cmap),num));
            obj.cmap = obj.cmap(ind,:);
        end
        function chopTo(obj,range)
            range = round(range*length(obj.cmap));
            ind = range(1)+1:1:range(2)-1;
            
            obj.cmap = obj.cmap(ind,:);
        end
        function hsv(obj,num)
            obj.cmap = hsv(num);
        end
        function jet(obj,num)
            obj.cmap = jet(num);
        end
        function hot(obj,num)
            obj.cmap = hot(num);
        end
        function cool(obj,num)
            obj.cmap = cool(num);
        end
        function gray(obj,num)
            obj.cmap = jet(num);
        end        
        
        function shuffle(obj)
            ind = randperm(length(obj.cmap));
            obj.cmap = obj.cmap(ind,:);
        end        
        function newBasis(obj,num,spaceStr)
            if nargin<3
                spaceStr = 'RGB';
            end
            vectBase = linspace(0,1,num);
            vect1 = reshape(repmat(vectBase,num^2,1),[],1);
            vect2 = reshape(repmat(reshape(repmat(vectBase,num,1),[],1),num,1),[],1);
            vect3 = repmat(vectBase(:),num^2,1);
            if strcmpi(spaceStr,'rgb')
                obj.cmap = [vect1 vect2 vect3];
            elseif strcmpi(spaceStr,'hsv')
                obj.cmap = hsv2rgb([vect1 vect2 vect3]);
            end
                
        end

        function [cmapHsv] = cmapInHsv(obj)
            
            cmapHsv = rgb2hsv(obj.cmap);
                
        end
        function removeBlacks(obj,tol)
            if nargin<2
                tol = 0.1;
            end
            ind = inBrightnessRange(obj.cmap,[0,tol]);
            
            obj.cmap = obj.cmap(~ind,:);
                
        end
        function removeWhites(obj,tol)
            if nargin<2
                tol = 0.1;
            end
            ind = inSaturationRange(obj.cmap,[0,tol]);
            
            obj.cmap = obj.cmap(~ind,:);
                
        end
        function sortByLuminance(obj)

            r = obj.cmap(:,1);
            g = obj.cmap(:,2);
            b = obj.cmap(:,3);
            lum = 0.30 *r + 0.59*g + 0.11*b;
            [garbage,ind] = sort(lum);
            
            obj.cmap = obj.cmap(ind,:);
        end
        
    end
    
    methods(Static)
        
        function[allColors] = allColors()
            flds = fieldnames(ColormapEditor.colorMat);
            allColors = ones(numel(flds),3);
            for idx = 1:numel(flds)
                allColors(idx,:) = ColormapEditor.colorMat.(flds{idx});
            end
                
        end
        
        function[allNames] = allNames()
            allNames = fieldnames(ColormapEditor.colorMat);
                            
        end
        
    end
    
end

function[ind] = inHueRange(map,range)
                                    
    hsvColors = rgb2hsv(map);
    h = hsvColors(:,1);

    ind = (range(1)<=h)&(h<=range(2));

end
function[ind] = inSaturationRange(map,range)

    hsvColors = rgb2hsv(map);
    s = hsvColors(:,2);

    ind = (range(1)<=s)&(s<=range(2));

end
function[ind] = inBrightnessRange(map,range)

    hsvColors = rgb2hsv(map);
    v = hsvColors(:,3);

    ind = (range(1)<=v)&(v<=range(2));

end
function[ind] = inRednessRange(map,range)

    r = map(:,1);

    ind = (range(1)<=r)&(r<=range(2));

end
function[ind] = inGreennessRange(map,range)

    g = map(:,2);

    ind = (range(1)<=g)&(g<=range(2));

end
function[ind] = inBluenessRange(map,range)
                                    
    b = map(:,3);

    ind = (range(1)<=b)&(b<=range(2));

end
function[ind] = findYellows(map,hueScale,brightTol)

    yelBot = 51/360;
    yelTop = 61/360;
    corr = (hueScale-1)*(yelTop-yelBot)/2;
    yelBot = yelBot - corr;
    yelTop = yelTop + corr;

    ind = inHueRange(map,[yelBot,yelTop]) & ...
        inBrightnessRange(map,[1-brightTol,1]);
end
function[ind] = findYellowsAlt(map,tol1,tol2)

    r = map(:,1);
    g = map(:,2);
    b = map(:,3);
    ind = (abs(r-g)<=tol1)&b<=tol2&r>=.4&g>=.4;

    
end
function[ind] = findGrays(map,tol)

    r = map(:,1);
    g = map(:,2);
    b = map(:,3);
    ind = (abs(r-g)<=tol)&(abs(r-b)<=tol)&(abs(b-g)<=tol);
end            

